controller.B.onEvent(ControllerButtonEvent.Pressed, function () {
    while (Dealer < Count) {
        Dealer += randint(1, 10)
        mySprite2.sayText(convertToText(Dealer))
        pause(200)
        if (Dealer > 21) {
            info.changeScoreBy(1)
            game.gameOver(true)
        }
    }
    info.changeScoreBy(-1)
    game.gameOver(false)
})
